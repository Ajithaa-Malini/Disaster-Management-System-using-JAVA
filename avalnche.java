private CategoryDataset createDataset(){
    final String Avalanche = "AVALANCHE";
    final String Tornado = "TORNADO";
    final String landslide = "LANDSLIDE";
    final String death = "Death";
    final String injured = "Injured";
    final String damages = "Damages";
   
    int deat= Integer.parseInt(dd);
      int injure= Integer.parseInt(i);
        int damage= Integer.parseInt(da);
    final DefaultCategoryDataset dataset = new DefaultCategoryDataset();
    dataset.addValue(deat,Avalanche,death);
     dataset.addValue(injure,Avalanche,injured);
      dataset.addValue(damage,Avalanche,damages);
    return dataset;
}
